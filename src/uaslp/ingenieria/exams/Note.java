package uaslp.ingenieria.exams;

public final class Note {
    private int note;
    private int tempo;

    public int getNote() {
        return note;
    }

    public void setNote(int nota) {
        this.note = nota;
    }

    public int getTempo() {
        return tempo;
    }

    public void setTempo(int tempo) {
        this.tempo = tempo;
    }
}
